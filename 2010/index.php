<script type="text/javascript">
$(document).ready(
function() {
    
    var $lightbox = $('#lightbox');
    var swing;
    
    /* Open surprise door */
    $('.shh').delegate('a', 'click', function() {
        var $this = $(this);
        var $shelf = $this.children(".shelf");
        if($this.hasClass("open")) {
            $this.removeClass("open").addClass("closed");
			clearInterval(swing); 
			$shelf.removeClass('swingleft swingright');
            setTimeout(removeclosed, 1000);
        } else {
            $('.shh a.open').removeClass("open").addClass("closed");
			clearInterval(swing);
			swing = setInterval(function() {
				if($shelf.hasClass('swingleft')) {
					$shelf.addClass('swingright').removeClass('swingleft');
				} else {
					$shelf.addClass('swingleft').removeClass('swingright');
				}
			}, 750);
            setTimeout(removeclosed, 1000);
            $this.addClass("open").removeClass("closed");
        }
    return false;
    });
 
    /* Operate door */
    $('.more, .vevent').delegate('a', 'click', function() {
        var $this = $(this);
    	$this.addClass("open").removeClass("closed");
		$('.shh a.open').removeClass("open").addClass("closed");

		var url = this.href + " .popup";
		
		var day = $this.find('.summary').html();
		if (day == $lightbox.attr('data-day')) {
			showPopup();
		} else {
			$this.siblings('.shelf').load(url, function() {
				$lightbox.attr('data-day', day);
				$lightbox.load(url, function() {
					showPopup();
				});
			});
		}
        
        return false;
    });
 
    
    /* Popup close door */
    $lightbox.delegate('a.close', 'click', function() {        
        $('#lightbox .popup').fadeTo(1000, 0, function() {
            $(this).removeClass("lightbox").css({
                "height":"auto",
                "paddingTop":"0"
            });
            setTimeout(closedoor, 1000);
        });

       return false;
    }); 
});

function showPopup() {
    var $document = $(document);
    var $popup = $('#lightbox .popup');
    		
	var docHeight = $document.height();
	var docHeightpx = docHeight + "px";
	
	var winHeight = $(window).height();
	var popHeight = $popup.height();
	var popScroll = $document.scrollTop();
	
	var popPos = (winHeight - popHeight)/2 + popScroll;
	var popPospx = popPos + "px";
	
	var bgPos = popPos - 37;
	var bgPospx = "center " + bgPos + "px";
	
	$popup.css({
	    "height": docHeightpx,
	    "paddingTop": popPospx,
	    "backgroundPosition": bgPospx
	}).addClass("lightbox").delay(750).fadeTo(2500,1);
}

function closedoor() {
    $('a.open').addClass("closed").removeClass("open");
    setTimeout(removeclosed, 1000);
}

function removeclosed() {
    $('a.closed').removeClass("closed");
} 

